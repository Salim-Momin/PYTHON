#Break function in for loop.

for i  in range(100):
    if(i==69):
        break # Exit the loop right now.
    print(i)

#Continue function in for loop.

for i  in range(100):
    if(i==69):
        continue # Skip this iteration.
    print(i)    

#Pass function in for loop. 

for i in range(100):
    pass    #Pass the function
i=0
while (i<=100):
    print(i)
    i+=1

    