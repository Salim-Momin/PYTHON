# For sort the list

L1 = [1,8,4,6,34,80,10,23,11,2]
L1.sort()
print(L1)

#List update into [1, 2, 4, 6, 8, 10, 11, 23, 34, 80]

# For reverse the list 

L1.reverse()
print(L1)

# For adds any number at the end of the list

L1.append(8)
print(L1)

# For adds 100 at 0 index

L1.insert(0,100)
print(L1)

# For delete element at index 11 and return its value.

L1.pop(11)
print(L1)

# For remove the value of in list

L1.remove(80)
print(L1)

