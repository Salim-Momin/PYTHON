#use for count the tuple

my_tuple = (1, 2, 3, 2, 4, 2)
print(my_tuple.count(2))  # Output: 3

#See you can add the tuple in python(tuple Concatenation)

a = (1, 2)
b = (3, 4)
c = a + b
print(c)  # Output: (1, 2, 3, 4)

#you can repeat the tuple (tuple repetition)

a = (1, 2)
print(a * 3)  # Output: (1, 2, 1, 2, 1, 2)

#tuple membership test

a = (1, 2, 3)
print(2 in a)     # Output: True
print(5 not in a) # Output: True

